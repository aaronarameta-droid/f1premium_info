// Current language is stored in localStorage. Defaults to Spanish (es) if not set.
let currentLang = localStorage.getItem('f1Lang') || 'es';

/**
 * Apply translations to all elements marked with the data-i18n attribute.
 * Also updates placeholders where data-i18n-placeholder is used.
 */
function applyTranslations() {
  const lang = currentLang;
  const dict = translations[lang] || translations['es'];
  // Set page language and direction
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  // Update text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) {
      // Remove citation markers like 【...】 from the translation before inserting
      let value = String(dict[key]);
      value = value.replace(/【[^】]+】/g, '');
      el.innerHTML = value;
    }
  });
  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key]) {
      let value = String(dict[key]);
      value = value.replace(/【[^】]+】/g, '');
      el.setAttribute('placeholder', value);
    }
  });

  // After updating text, update any localized images according to the current language.
  updateLocalizedImages();
}

/**
 * Change the active language.
 * @param {string} lang Language code
 */
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('f1Lang', lang);
  applyTranslations();
  // Update selection value if exists
  const selector = document.getElementById('lang-select');
  if (selector && selector.value !== lang) {
    selector.value = lang;
  }
}

/**
 * Update localized images for the current language.
 * Looks for <img> elements with a data-base attribute (which specifies the base filename
 * without extension) and swaps the src to the localized version under
 * assets/register/localized. If a localized image for the current language
 * does not exist, it falls back to the original base image path.
 */
function updateLocalizedImages() {
  const lang = currentLang;
  // Only run if document exists
  const images = document.querySelectorAll('img[data-base]');
  images.forEach(img => {
    const base = img.getAttribute('data-base');
    // Determine original file extension (assume .png)
    const localizedPath = `assets/register/localized/${base}_${lang}.png`;
    // When switching languages, update src. Because we cannot verify file existence
    // from the browser in a static site, we optimistically set the localized path.
    img.setAttribute('src', localizedPath);
  });
}

// FAQ toggling functionality
function initFAQ() {
  const items = document.querySelectorAll('.faq-item h3');
  items.forEach(header => {
    header.addEventListener('click', () => {
      const p = header.nextElementSibling;
      if (p.style.display === 'block') {
        p.style.display = 'none';
      } else {
        p.style.display = 'block';
      }
    });
  });
}

// Initialise translation and FAQ after DOM content loaded
document.addEventListener('DOMContentLoaded', () => {
  // Set the language selector value
  const selector = document.getElementById('lang-select');
  if (selector) {
    selector.value = currentLang;
    selector.addEventListener('change', e => setLanguage(e.target.value));
  }
  applyTranslations();
  initFAQ();
});
